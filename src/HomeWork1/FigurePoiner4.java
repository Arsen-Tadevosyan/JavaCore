package HomeWork1;

public class FigurePoiner4 {
    public static void main(String[] args) {
        char a = '*';
        System.out.println();
        for (int j = 0; j < 5; j++) {
            System.out.print(a+" ");
        }
        System.out.println();
        System.out.print("  ");
        for (int j = 0; j < 4; j++) {
            System.out.print(a+" ");
        }
        System.out.println();
        System.out.print("    ");
        for (int j = 0; j < 3; j++) {
            System.out.print(a + " ");
        }
        System.out.println();
        System.out.print("      ");
        for (int j = 0; j < 2; j++) {
            System.out.print(a + " ");
        }
        System.out.println();
        for (int j = 0; j < 1; j++) {
            System.out.print("        " + a);
        }    }
}
